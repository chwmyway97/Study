package loop.ex;

public class WhileEx1 {
    public static void main(String[] args) {
        int count = 0;
        while(count <10){
            count++;
            System.out.println(count);
        }
        System.out.println("//////////////");
        for (int count1 = 1; count1 <= 10; count1++) {
            System.out.println(count1);
        }
    }
}
